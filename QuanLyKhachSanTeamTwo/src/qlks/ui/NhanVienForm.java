    int index = -1;
    NhanVienDAO dao = new NhanVienDAO();

    void init() {
        setLocationRelativeTo(null);
        
        this.index = -1;
    }
    
    void insert() {
        NhanVien model = getForm();
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            DialogHelper.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
        }
    }
    
    void update(){
        NhanVien nv = getForm();
            try {
                dao.update(nv);
                this.fillTable();
                DialogHelper.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Thêm mới thất bại!");
            }
    }
    void delete(){
            if(DialogHelper.confirm(this, "Bạn có muốn xóa hay không?")){
                String nv = txtMaNV5.getText();
                try {
                    dao.delete(nv);
                    this.fillTable();
                    this.clearForm();
                    DialogHelper.alert(this, "Xóa thành công!");
                } 
                catch (Exception e) {
                    DialogHelper.alert(this, "Xóa thất bại!");
                }
            }
    }

    void clearForm(){
        txtChuThich6.setText("");
        txtChucVu5.setText("");
        txtDiaChi.setText("");
        txtLuong5.setText("");
        txtMaNV5.setText("");
        txtSoDienThoai.setText("");
        txtTenNV5.setText("");
        dateNgayBatDauLam.setDateFormatString("");
        dateNgaySinh.setDateFormatString("");
    }
    void setForm(NhanVien nv){
        txtMaNV5.setText(nv.getMaNhanVien());
        txtTenNV5.setText(nv.getTenNhanVien());
        txtChucVu5.setText(nv.getChucVu());
        txtLuong5.setText(Double.toString(nv.getLuong()));
        dateNgaySinh.setDate(nv.getNgaySinh());
        rdoNam.setSelected(nv.isGioiTinh());
        rabNu.setSelected(!nv.isGioiTinh());
        txtSoDienThoai.setText(nv.getSoDienThoai());
        txtDiaChi.setText(nv.getDiaChi());
        dateNgayBatDauLam.setDate(nv.getNgayBatDauLam());
    }
    NhanVien getForm(){
        NhanVien nv = new NhanVien();
        nv.setMaNhanVien(txtMaNV5.getText());
        nv.setTenNhanVien(txtTenNV5.getText());
        nv.setChucVu(txtChucVu5.getText());
        nv.setLuong(Double.parseDouble(txtLuong5.getText()));
        nv.setNgaySinh((Date) DateHelper.toDate(dateNgaySinh.getToolTipText()));
        nv.setNgayBatDauLam((Date) DateHelper.toDate(dateNgayBatDauLam.getToolTipText()));
        if (rdoNam.isSelected()) {
            nv.setGioiTinh(true);

        } else {
            nv.setGioiTinh(false);
        }
        nv.setSoDienThoai(txtSoDienThoai.getText());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setChuThich(txtChuThich6.getText());
        return nv;
    }
    private void timKiem() throws SQLException{
        this.fillTable();
        this.clearForm();
        this.index = -1;
        this.updateStatus();
    }
    void first(){
        this.index = 0;
        this.edit();
    }
    void prev(){
        if(this.index > 0){
            this.index--;
            this.edit();
        }
    }
    void next(){
        if(this.index < tblNhanVien.getRowCount() - 1){
            this.index++;
            this.edit();
        }
    }
    void last(){
        this.index = tblNhanVien.getRowCount() -1;
        this.edit();
    }
    void updateStatus(){
        boolean edit = this.index >= 0;
        boolean first = this.index == 0;
        boolean last = this.index == tblNhanVien.getRowCount() - 1;
        txtMaNV5.setEditable(!edit);
        //nvi insert thì ko update, delete
        btnThem1.setEnabled(!edit);
        btnLuu.setEnabled(edit);
        btnXoa.setEnabled(edit);
        //trạng thái điều hướng
        //btnFirst.setEnabled(edit && !first);
        //btnPrev.setEnabled(edit && !first);
        //btnNext.setEnabled(edit && !last);
        //btnLast.setEnabled(edit && !last);
    }
    void edit(){
        try {
            String maNV = (String)tblNhanVien.getValueAt(this.index, 0);
            NhanVien nv = dao.findById(maNV);
            if(nv != null){
                setForm(nv);
                tab.setSelectedIndex(0);
                updateStatus();
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
